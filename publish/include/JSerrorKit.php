function Mess() {}

Mess.facto=function(mes,extra,addedclass,removedclass) {
var plus='';
if(typeof extra != 'undefined') plus='<BR><TT>'+extra+'</TT>'; 
$("#MECONTENT").html(mes+plus);
$("#MESSAGE").removeClass(removedclass);
$("#MESSAGE").addClass(addedclass);
}

Mess.error=function(mes,extra) {
$('#MEVALID').hide();
Mess.facto(mes,extra,'ui-state-error','ui-state-highlight');
$("#MESSAGE").slideDown(500,function() {Mess.clear(5000)});
}

Mess.info=function(mes,extra) {
$('#MEVALID').hide();
Mess.facto(mes,extra,'ui-state-highlight','ui-state-error');
$('#MESSAGE').slideDown(500,function() {Mess.clear(1000)});
}

Mess.infostay=function(mes,extra) {
$('#MEVALID').show();
Mess.facto(mes,extra,'ui-state-highlight','ui-state-error');
$("#MESSAGE").slideDown(500);
}

Mess.errorstay=function(mes,extra) {
$('#MEVALID').show();
Mess.facto(mes,extra,'ui-state-error','ui-state-highlight');
$("#MESSAGE").slideDown(500);
}

Mess.clear=function(lapse) {
if(typeof lapse=='undefined') lapse=3000; 
$("#MESSAGE").fadeOut(lapse);
}

Mess.loading=function() {$('#LOADING').show();}
	
Mess.ready=function() {$('#LOADING').hide();}
	
$(function() {
	$(document).ajaxSend(function() {
		Mess.loading();
		});
	$(document).ajaxStop(function() {
		Mess.ready();
	});
	$('#MECLOSE').click(function() {
		Mess.clear(300);	
	});
})
